path = '/Users/oogatoshikatsu/Desktop/horizon-shield/hs-reverse-estimate/index.html'
with open(path, 'r', encoding='utf-8') as f:
    content = f.read()

# 「ヒアリング中の会話スタイル」セクション全体を強化
old = """# ヒアリング中の会話スタイル
・一度に1〜2個だけ質問する
・前の回答に一言共感してから次の質問へ
・途中で参考相場・注意点を伝えてもよい
・情報が不足していると感じたら「もう少し教えてもらえますか？」と聞く
・情報が十分でなくても、施主が「出してください」と言ったら必ずPLANを出力する"""

new = """# ヒアリング中の会話スタイル
【最優先・絶対遵守】返答する前に必ず以下を実行：
ステップ1: 会話履歴の全ユーザーメッセージを読み返す
ステップ2: 既に判明している情報を全て列挙（地域/工事種別/規模/築年数/構造/予算/時期/賃貸or自宅/世帯数/トイレ数/洗面台数/お風呂の階/キッチン数）
ステップ3: 列挙した情報に該当する質問は絶対に再度しない
ステップ4: 不足項目だけ1〜2個質問する。直前のassistantメッセージと類似の質問は絶対に出さない

・前の回答に一言だけ共感（10文字以内）してから質問
・施主が「出して」「プランを」「算出」「概算」「見せて」のいずれかを言ったら、不足項目があっても即PLANを出力
・同じ質問を2回した時点で失格。代わりに「では今ある情報でプランを出します」と言ってPLAN出力に進む"""

if old in content:
    content = content.replace(old, new, 1)
    with open(path, 'w', encoding='utf-8') as f:
        f.write(content)
    print("OK: 会話スタイル根本書き換え成功")
else:
    print("NG: 対象文字列なし")
